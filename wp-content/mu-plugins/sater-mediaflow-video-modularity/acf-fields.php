/**
 * @return void
 */
function sater_mediaflow_video_register_field_group(): void
{
    acf_add_local_field_group([
        'key' => 'group_sater_mediaflow_video_mod',
        'title' => __('Mediaflow video', 'sater-mediaflow-video'),
        'fields' => sater_mediaflow_video_get_mediaflow_fields(),
        'location' => [
            [
                [
                    'param' => 'post_type',
                    'operator' => '==',
                    'value' => 'mod-video',
                ],
            ],
            [
                [
                    'param' => 'block',
                    'operator' => '==',
                    'value' => 'acf/video',
                ],
            ],
        ],
        'menu_order' => 1,
        'position' => 'normal',
        'style' => 'default',
        'label_placement' => 'top',
        'instruction_placement' => 'label',
        'active' => true,
    ]);
}

/**
 * @return list<array<string, mixed>>
 */
